package com.lottogame2;

public class LottoGameRule {

	private int randomNumber;
	private int dap;
	private int win;
	private int lose;

	public int getRandomNumber() {
		return randomNumber;
	}

	public int getDap() {
		return dap;
	}

	public int getWin() {
		return win;
	}

	public int getLose() {
		return lose;
	}

}
